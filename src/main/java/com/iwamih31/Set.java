package com.iwamih31;

public class Set {
	public String name;
	public int width;

	public Set(String name, int width) {
		super();
		this.name = name;
		this.width = width;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

}
